     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request->filled('active')); //Para defeniel checkbox si es true o false
        $data = $request->all();
        DB::beginTransaction();
        
        try {
            $marca = new Marca;
            $marca->fill($data);
            $marca->active = $request->filled('active');
            $marca->save();
            
            DB::commit();
            return redirect()->route('marcas.index');

        }catch (\Exception $e) {
            DB::rollback();
        }

       
    }

